        std::ranges::iterator_t<R> begin;
        std::ranges::iterator_t<R> end;
        
        auto operator*() const { return *current; }
        
        iterator& operator++() {
            ++current;
            if (current == end) {
                current = begin;  // Ğ’Ğ¾Ğ·Ğ²Ñ€Ğ°Ñ‚ Ğº Ğ½Ğ°Ñ‡Ğ°Ğ»Ñƒ (Ñ†Ğ¸ĞºĞ»)
            }
            return *this;
        }
        
