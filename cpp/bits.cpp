int absNoBranch(int num) {
    int mask = num >> 31;
    return (num + mask) ^ mask;
}

// ---------------------------------------------------
// üìå –ú–∏–Ω–∏–º—É–º/–º–∞–∫—Å–∏–º—É–º –±–µ–∑ –≤–µ—Ç–≤–ª–µ–Ω–∏–π
