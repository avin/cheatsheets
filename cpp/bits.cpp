void generateSubsets(int n) {
    int totalSubsets = 1 << n;
    for (int mask = 0; mask < totalSubsets; ++mask) {
        std::cout << "–ü–æ–¥–º–Ω–æ–∂–µ—Å—Ç–≤–æ: ";
        for (int i = 0; i < n; ++i) {
            if (mask & (1 << i)) {
                std::cout << i << " ";
            }
        }
        std::cout << std::endl;
    }
}

